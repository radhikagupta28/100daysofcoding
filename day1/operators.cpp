#include<iostream>
using namespace std;

int main()
{
    int a=2.0/5;
    cout<<a<< endl;
    cout<< 2.0/5 <<endl;

    float b=2.0/5;
    cout<<b<<endl;


    //Relational operators(=,>,<,>=,<=,!=)
    int c=2;
    int d=6;

    bool first=(c==d);
    cout<<first<<endl;

    bool second=(c<=d);
    cout<<second<<endl;

    bool third=(c>=d);
    cout<<third<<endl;

    bool fourth=(c!=d);
    cout<<fourth<<endl;

    //logical operators(&&,||,!)
    int e=9;
    cout<<!e<<endl;
    int s=0;
    cout<<!s<<endl;
    

}